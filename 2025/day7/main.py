from collections import deque

def part1(diagram: list[str]) -> int:
  '''
  Returns number of times the tachyon beam gets splitted starting from S moving downward
  Can assume the are no adjacent splitters
  
  :param diagram: a diagram of the tachyon manifold
  :type diagram: list[str]
  :return: number of times the beam gets splitted
  :rtype: int
  '''

  m, n = len(diagram), len(diagram[0])

  start_x, start_y = None, None
  
  for i in range(m):
    for j in range(n):
      if diagram[i][j] == 'S':
        start_x, start_y = i, j
        break
    
    if start_x is not None:
      break

  queue = deque([(start_x, start_y)])
  visited = [[False] * n for _ in range(m)]
  visited[start_x][start_y] = True

  cnt = 0

  while queue:
    x, y = queue.popleft()

    if x == m - 1:
      continue

    if visited[x + 1][y]:
      continue

    if diagram[x + 1][y] == '.':
      queue.append((x + 1, y))
      visited[x + 1][y] = True
    else:
      cnt += 1

      if y > 0 and not visited[x + 1][y - 1]:
        visited[x + 1][y - 1] = True
        queue.append((x + 1, y - 1))

      if y + 1 < n and not visited[x + 1][y + 1]:
        visited[x + 1][y + 1] = True
        queue.append((x + 1, y + 1))

  return cnt

