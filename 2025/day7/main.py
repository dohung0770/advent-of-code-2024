from functools import cache

def part2(diagram: list[str]) -> int:
  m, n = len(diagram), len(diagram[0])

  @cache
  def dp(r: int, c: int) -> int:
    '''
    Returns number of possible timeline where the tachyon beam faces a splitter, it can be sent to the left or right.
    Can assume there are no adjacent splitters, and no splitters at the bottom row
    
    :param r: current beam's row
    :type r: int
    :param c: current beam's column
    :type c: int
    :return: number of possible timelines
    :rtype: int
    '''

    if r == m - 1:
      return 1

    # Out of bound
    if c < 0 or c >= n:
      return 0
    
    if diagram[r + 1][c] == '.':
      return dp(r + 1, c)
    
    return dp(r + 1, c - 1) + dp(r + 1, c + 1)


  start_x, start_y = None, None
  for i in range(m):
    for j in range(n):
      if diagram[i][j] == 'S':
        start_x, start_y = i, j
        break

    if start_x is not None:
      break

  return dp(start_x, start_y)

